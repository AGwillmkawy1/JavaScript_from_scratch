const prompt = require('prompt-sync')();
// lesson 02 - Build a Baby Weather App  ==> Now this is gonna introduce to us what we call Conditional statement.

let weather = prompt('How is the weather behaving?')

if (weather == 'rainy') {
    console.log('grab your umbrella')
} else {
    console.log('wear your sunglasses')
}



